export const GetProductsByCategory = async ({ title, category }: { title: string; category: number }): Promise<ApiResponse<Product>> => {
    let loading = true
    const response = await new Promise((resolve) => {
        setTimeout(
            () => {
                loading = true
                resolve([] as Product[])
            },
            5000,
            [] as Product[]
        )
    })

    const data = response as Product[]

    loading = false
    return {
        summaryInfo: {
            title,
            category,
        },
        loading,
        data: data,
        error: false,
    }
}
